#include "ft_math.h"

int	ft_is_prime(long long nb)
{
	int	f;

	if (nb < 2)
		return (0);
	f = 1;
	while (++f <= 46341 && f < nb)
	{
		if (nb % f == 0)
			return (0);
	}
	return (1);
}
